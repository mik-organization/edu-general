package Section01;

public class Sample07 {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		
		System.out.println("[x]:" + x + "[y:]" + y);
		System.out.println("等しい？:" + (x == y));
		System.out.println("等しくない？:" + (x != y));
		System.out.println("xはyより大きい？:" + (x > y));
		System.out.println("xはy以上？:" + (x >= y));
		System.out.println("yはxより大きい？:" + (x < y));
		System.out.println("yはx以上？:" + (x <= y));
		
	}

}
